package collada.internal;

public class CProfileCommon {

	private String sid;
	private String source;
	
	public CProfileCommon(String sid, String source) {
		this.sid = sid;
		this.source = source;
	}

	public String getSid() {
		return sid;
	}

	public String getSource() {
		return source;
	}
	
}
